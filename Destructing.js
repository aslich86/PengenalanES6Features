// Destructuring objek
const person = { name: "John", age: 30 };
const { name, age } = person;
console.log(name); // Output: "John"
console.log(age); // Output: 30

// Destructuring array
const numbers = [1, 2, 3];
const [x, y, z] = numbers;
console.log(x); // Output: 1
console.log(y); // Output: 2
console.log(z); // Output: 3
